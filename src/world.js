class World {
    // Singleton that represents the currently loaded world.
    
    constructor(width, height) {
        this.width = width;
        this.height = height;
        this.min_x = -width/2;
        this.max_x = width/2;
        this.min_y = -height/2;
        this.max_y = height/2;   
        this.player = undefined;
        this.camera = undefined; 
        this.entities = [];
        this.phys_entities = [];
        this.monsters = [];
        this.projectiles = [];
        this.texts = [];
        this.sprites = [];
        this.merchant = new Merchant();
        this.altar = new SacrificeAltar();
        this.map = new Map(); 
        this.collisions = new Collisions();
        this.spawn = new Spawner();
        this.despawn = new Despawner();
    }

    update() {

        // Collision detection
        this.collisions.run();

        // Entities self update
        this.entities.forEach(entity => {
            entity.update()
        });
                
        if (this.player.hp <= 0) {
            gg = true;
            this.player.hp = 0;
        }

        // Spawn/despawn
        this.spawn.run();
        this.despawn.run();
    }
}

